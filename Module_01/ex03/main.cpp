#include "HumanA.hpp"
#include "HumanB.hpp"

/* HumanA: reference to Weapon
HumanB: pointer to Weapon as initialized without Weapon and reference can not be NULL */

int main(){
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	return 0;
}
